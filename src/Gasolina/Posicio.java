package Gasolina;

public class Posicio {
    private int CoordX;
    private int CoordY;

    public Posicio(int cx, int cy) {
        this.CoordX = cx;
        this.CoordY = cy;
    }

    public int getCoordX() {
        return this.CoordX;
    }

    public void setCoordX(int CoordX) {
        this.CoordX = CoordX;
    }

    public int getCoordY() {
        return this.CoordY;
    }

    public void setCoordY(int CoordY) {
        this.CoordY = CoordY;
    }

}

